package max;

public class OTPBean {
	String mobile;
	String otp;
	int mint;
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getOtp() {
		return otp;
	}
	public void setOtp(String otp) {
		this.otp = otp;
	}
	public int getMint() {
		return mint;
	}
	public void setMint(int mint) {
		this.mint = mint;
	}
	

}
